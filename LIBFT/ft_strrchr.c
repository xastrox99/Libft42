#include"libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	while (i > 0)
	{
		if ((unsigned char)s[i] == (unsigned char)c)
			return ((char *)s + i);
		i--;
	}
	if (i == 0 && (unsigned char)s[i] == (unsigned char)c)
		return ((char *)(s + i));
	return (NULL);
}
